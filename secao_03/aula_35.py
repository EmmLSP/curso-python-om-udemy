# while - Condição em detalhes

'''
Repetições
while (enquanto)
Executa uma acao enquanto uma condicao e verdadeira
Loop infinito -> quando um codigo nao tem fim
'''

contador = 0
while contador < 10:
    print(contador)
    contador += 1

print('...')

contador = 0
while contador <= 10:
    contador += 1
    print(contador)

print('...')
