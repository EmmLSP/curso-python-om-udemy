# Tipos de Dados - Strings

"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

#  Aspas simples
print('Otavio Miranda')
print(1, 'Otavio "Miranda"')

# Aspas duplas
print("Otavio Miranda")
print(2, "Otavio 'Miranda'")

# Escape -> pula "" e exibe ""
print("Otavio \"Miranda\"")

# r -> exibir caractere de escape com aspa
print(r"Otavio \"Miranda\"")

# Escape -> pula '' e exibe ''
print('Otavio \'Miranda\'')

# r -> exibir caractere de escape com aspa
print(r'Otavio \'Miranda\'')
